          C. 서버에 대한 제3자의 모든 불법적인 접속 또는 서버의 불법적인 이용으로 부터 발생하는 손해<br>
          D. 서버에 대한 전송 또는 서버로부터의 전송에 대한 제3자의 모든 불법적인 방해 또는 중단행위로부터 발생하는 손해<br>
          E. 제3자가 “서비스”를 이용하여 불법적으로 전송, 유포하거나 또는 전송, 유포되도록 한 모든 바이러스, 스파이웨어 및 기타 악성 프로그램으로 인한 손해<br>
          F. 전송된 데이터의 오류 및 생략, 누락, 파괴 등으로 발생되는 손해<br>
          G. “회원”간의 “회원”상태정보 등록 및 “서비스”이용과정에서 발생하는 명예훼손 기타 불법행위로 인한 각종 민형사상 책임
