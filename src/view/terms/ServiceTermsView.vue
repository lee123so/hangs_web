          ⑤ “회사”는 “회원”간 또는 “회원”과 제3자 상호간에 “서비스”를 매개로 하여 거래 등을 한 경우에는 책임이 면제됩니다.<br>
          ⑥ “회사”는 무료로 제공되는 “서비스”이용과 관련하여 관련법에 특별한 규정이 없는 한 책임을 지지 않습니다.<br>
          ⑦ “회사”는 제3자가 서비스내 화면 또는 링크된 웹사이트를 통하여 광고한 제품 또는 서비스의 내용과 품질에 대하여 감시할 의미 기타 어떠한 책임도 지지 아니합니다.<br>
          ⑧ “회사”및 “회사”의 임직원 그리고 대리인은 다음과 같은 사항으로부터 발생하는 손해에 대해 책임을 지지 아니합니다.
        </p>
        <span>
