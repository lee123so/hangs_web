          ③ “회원”이 계약을 해지하는 경우, 개인형 서비스 내에 게재된 게시물은 일체 삭제됩니다. 단, 공용 서비스내에 게재된 게시물과 다른 회원 또는 제3자에 의하여 다시 게시된 게시물, 공유게시판, 댓글 등 다른 회원의 정상적인 서비스 이용에 필요한 게시물은 삭제되지 않습니다.
