          ③ “회원”이 “서비스”내 올린 게시물은 스탬프카드와 회사가 운영하는 타 서비스에 함께 게시될 수 있습니다.<br>
          ④ “회사”는 제2항, 제3항 이외의 방법으로 “회원”의 “게시물”을 이용하고자 하는 경우에는 사전에 “회원”의 동의를 얻어야 합니다.
