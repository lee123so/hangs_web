          ② “회사”는 “서비스”와 관련하여 “회원”에게 “회사”가 정한 이용조건에 따라 콘텐트 등을 이용할 수 있는 이용권만을 부여하며, “회원”은 이를 양도, 판매, 담보제공 등의 처분행위를 할 수 없습니다.
        </p>

