          ② “회사”는 “서비스”의 운영과 관련하여 서비스 화면, 홈페이지, 알림(푸시) 등에 광고를 게재할 수 있습니다. 광고가 게재된 메시지를 수신한 “회원”은 수신거절을 “회사”에게 할 수 있습니다.
