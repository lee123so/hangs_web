        </p>
        <span>
          A. 개인위치정보를 수집한 당해 통신단말장치가 문자, 음성 도는 영상의 수신기능을 갖추지 아니한 경우<br>
          B. 개인위치정보주체가 온라인 게시 등의 방법으로 통보할 것을 미리 요청한 경우
