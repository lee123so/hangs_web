          ⑤ “회사”는 “회원”이 “회사”가 승인하지 않은 방법으로 “쿠폰”을 획득하거나 부정한 목적이나 용도로 사용하는 경우 “쿠폰”의 사용을 제한하거나 “회원”자격을 정지할 수 있습니다.<br>
          ⑥ “회원” 탈퇴 시 “서비스” 구매 후 발급받은 “쿠폰”중 미사용한 “쿠폰”은 즉시 소멸되며, 탈퇴 후 재가입하더라도 소멸된 “쿠폰”은 복구되지 아니합니다.
