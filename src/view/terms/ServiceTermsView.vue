          ② “회사”는 “쿠폰”의 사용 대상, 사용방법, 사용기간, 할인금액 등을 “쿠폰” 또는 “서비스”화면에 별도로 표시하거나 통지합니다.<br>
          ③ “쿠폰”은 “서비스”의 사용 유효기간 또는 “쿠폰”의 사용기간이 만료되면 이용 여부와 관계없이 모두 소멸됩니다.<br>
